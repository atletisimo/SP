/*

Напиши програма која ќе внесе низа (не подолга од 100 карактери) која содржи букви и цифри. Програмата треба да отпечати на стандардниот излез буквите што се наоѓаат помеѓу првата и втората цифра, од лево кон десно, вклучувајќи ги и тие две цифри.

Ако во низата има само една цифра, треба да се отпечатат сите букви од таа цифра до крајот на низата.

Ако нема цифри во низата, да се отпечати порака: "No digits".


  */


#include <iostream>
#include <cstring>
using namespace std;

int main() {
    char niz[101];
    cin.getline(niz, 101);

    int len = strlen(niz);
    int firstDigitIndex = -1;
    int secondDigitIndex = -1;

    // Наоѓаме индекс на првата и втората цифра
    for (int i = 0; i < len; i++) {
        if (niz[i] >= '0' && niz[i] <= '9') {
            if (firstDigitIndex == -1) {
                firstDigitIndex = i;
            } else {
                secondDigitIndex = i;
                break; // не ни требаат повеќе од две цифри
            }
        }
    }

    // Ако нема цифри
    if (firstDigitIndex == -1) {
        cout << "No digits" << endl;
        return 0;
    }

    // Ако има само една цифра, отпечатуваме од таа позиција до крај
    if (secondDigitIndex == -1) {
        for (int i = firstDigitIndex; i < len; i++) {
            cout << niz[i];
        }
        cout << endl;
        return 0;
    }

    // Ако има две цифри, отпечатуваме од првата до втората (вклучувајќи ги)
    for (int i = firstDigitIndex; i <= secondDigitIndex; i++) {
        cout << niz[i];
    }
    cout << endl;

    return 0;
}

