#include <iostream>
using namespace std;

int main() {
    char c;
    int sum = 0;
    int number = 0;

    while (true) {
        cin >> noskipws >> c; // Чита еден по еден карактер, без прескокнување на празни места (whitespace)
        if (c == '!') break;  // Ако наиде на '!', прекинува го циклусот

        // Ако карактерот е цифра (и не е празно место)
        if (isdigit(c) && !isspace(c)) {
            number = (number * 10) + (c - '0'); // Градење на тековен број
        } else {
            sum += number;  // Ако карактерот не е цифра, додај го тековниот број во збир
            number = 0;     // Ресетирај ја бројката
        }
    }

    // Доколку последниот број не е додаден (ако низата завршува со број)
    if (number != 0) sum += number;

    cout << sum;
}
