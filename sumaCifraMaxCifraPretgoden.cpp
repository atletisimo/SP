/*
*Од тастатура се читаат непознат број позитивни цели броеви се додека не се внесе нешто различно од број.
*За секој број треба да се пресмета збирот на цифрата со максимална вредност од претходно внесениот број
*и сумата на цифрите на тековниот број (за првиот внесен број се пресметува само сумата на неговите цифри).
*За секој внесен број резултатот треба да се испечати во следниот формат:

[број]: [сумата на цифрите на бројот + максималната цифра од претходниот број]

342: 9   //3+4+2

345: 16 //3+4+5+4

123: 11 //1+2+3+5

456: 18 //4+5+6+3

 */
#include <iostream>
using namespace std;

// Функција за збир на цифри
int sumaCifri(int n) {
    int sum = 0;
    while (n > 0) {
        sum += n % 10;
        n /= 10;
    }
    return sum;
}

// Функција за најголема цифра
int maxCifra(int n) {
    int maxDigit = 0;
    while (n > 0) {
        int c = n % 10;
        if (c > maxDigit) {
            maxDigit = c;
        }
        n /= 10;
    }
    return maxDigit;
}

int main() {
    int broj;
    int prethoden = 0;   // Ќе чува претходен број (валиден број, не индикатор)
    bool first = true;   // Знаеме дали сме на првиот број

    while (cin >> broj) {
        int zbir = sumaCifri(broj);

        if (first) {
            // Првиот број – нема претходен
            cout << broj << ": " << zbir << endl;
            first = false;
        } else {
            // Останатите – додади најголема цифра од претходниот
            zbir += maxCifra(prethoden);
            cout << broj << ": " << zbir << endl;
        }

        prethoden = broj;  // Тековниот број станува претходен
    }

    return 0;
}
