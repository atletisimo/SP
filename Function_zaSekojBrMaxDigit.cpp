#include <iostream>
using namespace std;

// Итеративна функција за најголема цифра
int maxDigit(int n) {
    if (n < 0) n = -n; // за негативни броеви
    int maxD = 0;
    if (n == 0) return 0; // ако бројот е 0, најголемата цифра е 0
    while (n > 0) {
        int last = n % 10;
        if (last > maxD) maxD = last;
        n /= 10;
    }
    return maxD;
}

int main() {
    int num;
    while (cin >> num) { // чита додека има цели броеви
        cout << maxDigit(num) << endl;
    }
    return 0;
}

